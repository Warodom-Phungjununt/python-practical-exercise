# สมมติว่าเด็กหญิงจินนี่ มีส่วนสูงในปีที่แล้วและปีปัจจุบันดังนี้
height_last_year = 140.5
height_this_year = 143.5
# จงคำนวณอัตราการเติบโตของส่วนสูง
# และ print แสดงผลออกมาในรูปแบบดังนี้
# "เด็กหญิงจินนี่สูงขึ้น ____ %"
# โดยให้แสดงผลลัพธ์เป็น % ที่มีทศนิยม 3 ตำแหน่ง
growth_rate = (height_this_year-height_last_year)/height_last_year
print(f"เด็กหญิงจินนี่สูงขึ้น {growth_rate:.3%}")