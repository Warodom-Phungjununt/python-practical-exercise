# Generate the condition of the game by elif
# If kills is more or equal to 7, print 'Godlike!'
# If kills is less than 7 but more or equal to 5, print 'Unstoppable'
# If it is not, print 'Noob!!!'

kills = 6

if kills >= 7:
    print('Godlike!')
elif kills >= 5:
    print('Unstoppable')
else:
    print('Noob!!!')